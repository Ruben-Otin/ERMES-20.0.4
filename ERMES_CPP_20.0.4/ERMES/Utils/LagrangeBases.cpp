    //********************************************************************************************************************
    //* - 1st order Lagrange base for 3D tetrahedrons
    //********************************************************************************************************************
	void Lagrange3D_Ni_1st( Matrix<double>& N, Vector<double>& cX, Vector<double>& cY, Vector<double>& cZ )
	{
		int nPoints = cX.size();

		N.Resize( 4, nPoints );
		
		double L1, L2, L3, L4;

        for( int p=0; p<nPoints; p++ ) 
        {
            L2 = cX[ p ];
            L3 = cY[ p ];
			L4 = cZ[ p ];
            L1 = 1.0 - L2 - L3 - L4;

			N[ 0 ][ p ] = L1;
            N[ 1 ][ p ] = L2;
            N[ 2 ][ p ] = L3;
			N[ 3 ][ p ] = L4;
        }
	}

