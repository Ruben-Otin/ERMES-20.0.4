	//********************************************************************************************************************
    //* - 1st order Lagrange + 1 inner bubble + 1 surface bubble for 3D tetrahedrons 
    //********************************************************************************************************************
	void Lagrange3D_Ni_1sb( Matrix<double>& N, Vector<double>& cX, Vector<double>& cY, Vector<double>& cZ )
	{
		int nPoints = cX.size();

		N.Resize( 9, nPoints );
		
		double L1, L2, L3, L4;

        for( int p=0; p<nPoints; p++ ) 
        {
            L2 = cX[ p ];
            L3 = cY[ p ];
			L4 = cZ[ p ];
            L1 = 1.0 - L2 - L3 - L4;

			// Corner nodes
			N[ 0 ][ p ] = L1;
            N[ 1 ][ p ] = L2;
            N[ 2 ][ p ] = L3;
			N[ 3 ][ p ] = L4;

			// Face bubbles 
			N[ 4 ][ p ] = 27.0 * L1 * L2 * L3;
            N[ 5 ][ p ] = 27.0 * L1 * L2 * L4;
            N[ 6 ][ p ] = 27.0 * L2 * L3 * L4;
			N[ 7 ][ p ] = 27.0 * L1 * L3 * L4;

			// Volume bubble 
			N[ 8 ][ p ] = 256.0 * L1 * L2 * L3 * L4;
        }
	}

