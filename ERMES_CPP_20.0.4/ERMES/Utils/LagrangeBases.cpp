	//********************************************************************************************************************
    //* - 4th order Lagrange base for 3D tetrahedrons
    //********************************************************************************************************************
	void Lagrange3D_Ni_4th( Matrix<double>& N, Vector<double>& cX, Vector<double>& cY, Vector<double>& cZ )
	{
		int nPoints = cX.size();

		N.Resize( 35, nPoints );
		
		double L1, L2, L3, L4;

        for( int p=0; p<nPoints; p++ ) 
        {
			L2 = cX[ p ];
            L3 = cY[ p ];
			L4 = cZ[ p ];
            L1 = 1.0 - L2 - L3 - L4;

			// Points: 0 1 2 3 4 
			N[ 0 ][ p ] = ( 1.0 / 6.0 ) * L1 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 ) * ( 4.0 * L1 - 3.0 );
            N[ 1 ][ p ] = ( 1.0 / 6.0 ) * L2 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 ) * ( 4.0 * L2 - 3.0 );
            N[ 2 ][ p ] = ( 1.0 / 6.0 ) * L3 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 ) * ( 4.0 * L3 - 3.0 );
			N[ 3 ][ p ] = ( 1.0 / 6.0 ) * L4 * ( 4.0 * L4 - 1.0 ) * ( 4.0 * L4 - 2.0 ) * ( 4.0 * L4 - 3.0 );

			// Edge: [0 1] 
			N[ 4 ][ p ] = ( 8.0 / 3.0 ) * L1 * L2 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 );
            N[ 5 ][ p ] = ( 4.0 )       * L1 * L2 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L2 - 1.0 );
            N[ 6 ][ p ] = ( 8.0 / 3.0 ) * L1 * L2 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 );

			// Edge: [1 2] 
            N[ 7 ][ p ] = ( 8.0 / 3.0) * L2 * L3 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 );
            N[ 8 ][ p ] = ( 4.0 )      * L2 * L3 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L3 - 1.0 );
            N[ 9 ][ p ] = ( 8.0 / 3.0) * L2 * L3 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 );

			// Edge: [2 0] 
            N[ 10 ][ p ] = ( 8.0 /3.0 ) * L3 * L1 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 );
            N[ 11 ][ p ] = ( 4.0 )      * L3 * L1 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L1 - 1.0 );
            N[ 12 ][ p ] = ( 8.0 /3.0 ) * L3 * L1 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 );

			// Edge: [0 3] 
			N[ 13 ][ p ] = ( 8.0 / 3.0 ) * L1 * L4 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 );
            N[ 14 ][ p ] = ( 4.0 )       * L1 * L4 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L4 - 1.0 );
            N[ 15 ][ p ] = ( 8.0 / 3.0 ) * L1 * L4 * ( 4.0 * L4 - 1.0 ) * ( 4.0 * L4 - 2.0 );

			// Edge: [1 3] 
            N[ 16 ][ p ] = ( 8.0 / 3.0 ) * L2 * L4 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 );
            N[ 17 ][ p ] = ( 4.0 )       * L2 * L4 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L4 - 1.0 );
            N[ 18 ][ p ] = ( 8.0 / 3.0 ) * L2 * L4 * ( 4.0 * L4 - 1.0 ) * ( 4.0 * L4 - 2.0 );

			// Edge: [2 3] 
			N[ 19 ][ p ] = ( 8.0 / 3.0 ) * L3 * L4 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 );
            N[ 20 ][ p ] = ( 4.0 )       * L3 * L4 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L4 - 1.0 );
            N[ 21 ][ p ] = ( 8.0 / 3.0 ) * L3 * L4 * ( 4.0 * L4 - 1.0 ) * ( 4.0 * L4 - 2.0 );
			
			// Face: [0 1 2] 
			N[ 22 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L1 - 1.0 );
            N[ 23 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L2 - 1.0 );
            N[ 24 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L3 - 1.0 );

			// Face: [0 1 3] 
            N[ 25 ][ p ] = ( 32.0 ) * L1 * L2 * L4 * ( 4.0 * L1 - 1.0 );
            N[ 26 ][ p ] = ( 32.0 ) * L1 * L2 * L4 * ( 4.0 * L2 - 1.0 );
            N[ 27 ][ p ] = ( 32.0 ) * L1 * L2 * L4 * ( 4.0 * L4 - 1.0 );

			// Face: [1 2 3] 
			N[ 28 ][ p ] = ( 32.0 ) * L2 * L3 * L4 * ( 4.0 * L2 - 1.0 );
            N[ 29 ][ p ] = ( 32.0 ) * L2 * L3 * L4 * ( 4.0 * L3 - 1.0 );
            N[ 30 ][ p ] = ( 32.0 ) * L2 * L3 * L4 * ( 4.0 * L4 - 1.0 );

			// Face: [0 2 3] 
			N[ 31 ][ p ] = ( 32.0 ) * L1 * L3 * L4 * ( 4.0 * L1 - 1.0 );
            N[ 32 ][ p ] = ( 32.0 ) * L1 * L3 * L4 * ( 4.0 * L3 - 1.0 );
            N[ 33 ][ p ] = ( 32.0 ) * L1 * L3 * L4 * ( 4.0 * L4 - 1.0 );

			// Volume: [0 1 2 3 4] 
			N[ 34 ][ p ] = ( 256.0 ) * L1 * L2 * L3 * L4;
        }
	}

	//********************************************************************************************************************
    //* - 4th order Lagrange base for 2D triangles
    //********************************************************************************************************************
	void Lagrange2D_Ni_4th( Matrix<double>& N, Vector<double>& cX, Vector<double>& cY )
	{
		int nPoints = cX.size();

		N.Resize( 15, nPoints );
		
		double L1, L2, L3;

        for( int p=0; p<nPoints; p++ ) 
        {
			L2 = cX[ p ];
            L3 = cY[ p ];
            L1 = 1.0 - L2 - L3;
            
            // Points: 0 1 2 
            N[ 0 ][ p ] = ( 1.0 / 6.0 ) * L1 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 ) * ( 4.0 * L1 - 3.0 );
            N[ 1 ][ p ] = ( 1.0 / 6.0 ) * L2 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 ) * ( 4.0 * L2 - 3.0 );
            N[ 2 ][ p ] = ( 1.0 / 6.0 ) * L3 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 ) * ( 4.0 * L3 - 3.0 );

            // Edge: [0 1] 
            N[ 3 ][ p ] = ( 8.0 / 3.0 ) * L1 * L2 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 );
            N[ 4 ][ p ] = ( 4.0 )       * L1 * L2 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L2 - 1.0 );
            N[ 5 ][ p ] = ( 8.0 / 3.0 ) * L1 * L2 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 );

            // Edge: [1 2] 
            N[ 6 ][ p ] = ( 8.0 / 3.0 ) * L2 * L3 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L2 - 2.0 );
            N[ 7 ][ p ] = ( 4.0 )       * L2 * L3 * ( 4.0 * L2 - 1.0 ) * ( 4.0 * L3 - 1.0 );
            N[ 8 ][ p ] = ( 8.0 / 3.0 ) * L2 * L3 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 );

            // Edge: [2 0] 
            N[  9 ][ p ] = ( 8.0 / 3.0 ) * L1 * L3 * ( 4.0 * L3 - 1.0 ) * ( 4.0 * L3 - 2.0 );
            N[ 10 ][ p ] = ( 4.0 )       * L1 * L3 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L3 - 1.0 );
            N[ 11 ][ p ] = ( 8.0 / 3.0 ) * L1 * L3 * ( 4.0 * L1 - 1.0 ) * ( 4.0 * L1 - 2.0 );

            // Face 
            N[ 12 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L1 - 1.0 );
            N[ 13 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L2 - 1.0 );
            N[ 14 ][ p ] = ( 32.0 ) * L1 * L2 * L3 * ( 4.0 * L3 - 1.0 ); 	
        }
	}

