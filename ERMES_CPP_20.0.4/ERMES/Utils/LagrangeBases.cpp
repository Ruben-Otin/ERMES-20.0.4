 	//********************************************************************************************************************
    //* - 2nd order Lagrange base for 3D tetrahedrons
 	//********************************************************************************************************************
	void Lagrange3D_Ni_2nd( Matrix<double>& N, Vector<double>& cX, Vector<double>& cY, Vector<double>& cZ )
	{
		int nPoints = cX.size();

		N.Resize( 10, nPoints );
		
		double L1, L2, L3, L4;

        for( int p=0; p<nPoints; p++ ) 
        {
            L2 = cX[ p ];
            L3 = cY[ p ];
			L4 = cZ[ p ];
            L1 = 1.0 - L2 - L3 - L4;

			// Points: 0 1 2 3 4 
			N[ 0 ][ p ] = L1 * ( 2.0 * L1 - 1.0 );
            N[ 1 ][ p ] = L2 * ( 2.0 * L2 - 1.0 );
            N[ 2 ][ p ] = L3 * ( 2.0 * L3 - 1.0 );
			N[ 3 ][ p ] = L4 * ( 2.0 * L4 - 1.0 );

			// Edge: [0 1] 
			N[ 4 ][ p ] = 4.0 * L1 * L2;
			
			// Edge: [1 2] 
			N[ 5 ][ p ] = 4.0 * L2 * L3;
            
			// Edge: [2 0] 
			N[ 6 ][ p ] = 4.0 * L3 * L1;
            
			// Edge: [0 3] 
			N[ 7 ][ p ] = 4.0 * L1 * L4;
			
			// Edge: [1 3] 
			N[ 8 ][ p ] = 4.0 * L2 * L4;

			// Edge: [2 3] 
			N[ 9 ][ p ] = 4.0 * L3 * L4;	
        }
	}

